package lab1.task3;

public class Calculator {
    double unu;
    double doi;
    double trei;

    double getAverageMedia (double unu, double doi) {
        return (unu + doi)/2;
    }
    double getAverageUniMedia (double unu, double doi, double trei) {
        return (unu + doi + trei)/3;
    }
}
